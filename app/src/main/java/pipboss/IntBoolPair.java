package pipboss;

public class IntBoolPair {
    private int x;
    private boolean b;
    
    public IntBoolPair(int x, boolean b)    {
        this.x = x;
        this.b = b;
    }

    public int getInt() {
        return x;
    }

    public boolean getBool()    {
        return b;
    }
}
